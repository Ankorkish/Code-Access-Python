# Konwersja liczb
# Konwersja z int do float
a_int = 5
a_float = float(a_int)
print(a_float) # 5.0

# Konwersja z float do int
b_float = 5.0
b_int = int(b_float)
print(b_int) # 5

# Konwersja z int do str
c_int = 5
c_str = str(c_int)
print(c_str) # '5'

# Konwersja z str do int
d_str = '5'
d_int = int(d_str)
print(d_int) # 5

# Konwersja z str do float
e_str = '5.0'
e_float = float(e_str)
print(e_float) # 5.0

