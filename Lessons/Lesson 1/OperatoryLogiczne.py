a = 10
b = 5
c = 20

# Operator and zwraca True, jeżeli oba warunki są spełnione
print(a > b and b < c)  # Wynik: True

# Operator or zwraca True, jeżeli przynajmniej jeden warunek jest spełniony
print(a < b or b < c)  # Wynik: True

# Operator not zwraca przeciwną wartość danego wyrażenia
print(not(a > b and b < c))  # Wynik: False
