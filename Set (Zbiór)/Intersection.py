# Tworzymy dwa zbiory
pierwszy_zbior = {1, 2, 3, 4, 5}
drugi_zbior = {4, 5, 6, 7, 8}

# Używamy metody intersection() do znalezienia wspólnych elementów
wspolne_elementy = pierwszy_zbior.intersection(drugi_zbior)

# Wyświetlamy wyniki
print("Pierwszy Zbiór:", pierwszy_zbior)
print("Drugi Zbiór:", drugi_zbior)
print("Wspólne Elementy:", wspolne_elementy)
