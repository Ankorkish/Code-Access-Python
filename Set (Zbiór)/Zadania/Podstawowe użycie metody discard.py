"""
Treść zadania: Napisz program w Pythonie, który tworzy zbiór moj_zbior z elementami
1, 2, 3, 4, 5, a następnie używa metody discard() do usunięcia elementu 3.
Wydrukuj zbiór po usunięciu elementu.
"""

# Tworzenie zbioru
moj_zbior = {1, 2, 3, 4, 5}

# Usunięcie elementu ze zbioru
moj_zbior.discard(3)

# Wyświetlenie zbioru
print(moj_zbior)
